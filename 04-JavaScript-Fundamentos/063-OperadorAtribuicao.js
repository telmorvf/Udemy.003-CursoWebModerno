const a = 7;
let b = 3;

b += a              // b = b + a
console.log(b);     // Print 10

b -= 4              // b = b - 4
console.log(b);     // Print 6

b *= 2              // b = b * 2
console.log(b);     // Print 12

b /= 2              // b = b / 2
console.log(b);     // Print 6

b %= 2              // b = b % 2
console.log(b);     // Print 0