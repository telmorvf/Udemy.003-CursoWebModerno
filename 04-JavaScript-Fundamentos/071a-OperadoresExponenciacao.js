// Operador Exponenciação
// ES7

// 1. Exponenciação da maneira antiga:
let exp1 = Math.pow(2,3);
console.log(exp1);      // 8

// 2. Com o ES7
let exp2 = 2 ** 3;
console.log(exp2);      // 8

// 3. Com o ES7
let base = 2;
base **= 3;
console.log(base);      // 8

// 4. Com o ES7
let exp4 = 2 ** -3;
console.log(exp4);      // 0.125